	<h1>Why is It Needed?</h1>

	<p>The Great Commission (Matthew 28:18-20) enjoins the making of disciples of all nations by baptism in the name of the Trinity and by handing on the teaching of Christ – a ministry of spiritual formation in the essentials of doctrine that is called catechesis. Largely because the ancient tradition of catechesis has declined, the faith of many young believers today is shallow and rootless, with little grounding in the Bible or doctrine, and little meaning for their lives. There is widespread agreement that a revival of the tradition of catechesis is necessary to the health and growth of the Church. “Train up”—catechize—“a child in the way he should go: and when he is old, he shall not depart from it” (Proverbs 22:6).</p>

	<h2>This is the need for the <i>Common Prayer Catechesis Project</i>.</h2>

	<p>With curriculum grounded in the Bible and the distinctives of the Anglican tradition of faith and worship, the <i>Common Prayer Catechesis Project</i> will equip churches to catechize their members and newcomers, an important step in fulfilling our calling to “make disciples of all nations.” The curriculum is designed to work with a wide range of churchmanship and liturgical custom, from the 1662 Prayer Book to the 1979 and modern-language adaptations. Its teaching will ground adults, youth, and children in the Catechism, the Bible, Common Prayer and the Sacraments—the essentials of Christian believing, living, and praying.</p>

	<h2>CPCP and Families</h2>

	<p>According to the Bible, Christian parents have a unique gift and vocation to form the faith of their children. In Psalm 78 we read that God:</p>

	<blockquote class="p"><i>“made a covenant with Jacob, and gave Israel  a law, which he commanded our forefathers  to teach their children; / That their posterity might know it, and the children which were yet unborn; / To the intent that when they came up, they might show their children the same; / That they might put their trust in God; and not to forget the works of God, but to keep his commandments.”</i></blockquote>

	<p>Similar teaching is found in Exodus 13:8, Deuteronomy 6:6-9, Proverbs 4:1-13, and Ephesians 6:4. According to the 1662 Prayer Book, one of the causes for which Marriage was ordained by God was “for the procreation of children to be brought up in the fear and nurture of the Lord, and to the praise of his holy Name.” The <i>Common Prayer Catechesis Project</i> will equip Christian parents (with the help of their clergy and other teachers) to catechize their children in the essentials of Christian believing, living, and praying, within the fellowship and worship of the congregation.</p>
